export function getPark(parkId: string): Park | undefined {
  return PARKS.find((p) => p.id === parkId);
}

export function getRidesForPark(parkId: ParkId): Ride[] {
  return RIDES.filter((r) => r.parkId === parkId);
}

export function getRide(rideId: string): Ride | undefined {
  return RIDES.find((r) => r.id === rideId);
}
